void DisplayASCII()
{
    int iCnt=0;
    printf("Decimal\tHex\tChar\n");
    for(iCnt=0;iCnt<=255;iCnt++)
    { 
     // if(iNo==iCnt)  // to print single ASCII value
      {
        if (iCnt < 32 || iCnt == 127) 
       { 
          //%3d=decimal,%02X=hexadecima
          printf("%3d\t%02X\t------\n", iCnt, iCnt);
        } 
        else
         {//                  %c=character
            printf("%3d\t%02X\t%c\n", iCnt, iCnt, iCnt);
         }
      }
    }
}

